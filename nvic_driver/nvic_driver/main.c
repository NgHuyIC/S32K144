/**
 * @brief   Main function
 * @details Initializes NVIC for PORTC and PORTA interrupts, sets priorities, and demonstrates enable/disable.
 *
 * @return  int
 */
int main() {
    NVIC_EnableInterrupt(PORTC_IRQn);    // Enable interrupt for PORTC
    NVIC_SetPriority(PORTC_IRQn, 2);     // Set priority for PORTC interrupt

    NVIC_EnableInterrupt(PORTA_IRQn);    // Enable interrupt for PORTA
    NVIC_SetPriority(PORTA_IRQn, 8);     // Set priority for PORTA interrupt

    NVIC_DisableInterrupt(PORTA_IRQn);   // Disable interrupt for PORTA
    NVIC_DisableInterrupt(PORTC_IRQn);   // Disable interrupt for PORTC

    while (1) {
        // Infinite loop to keep the program running
    }
}
